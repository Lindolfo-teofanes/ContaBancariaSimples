public class Conta {
    Integer conta;
    String agente;
    String nome;
    Double saldo;

    public Conta() {
    }

    public Conta(Integer conta, String agente, String nome, Double saldo) {
        this.conta = conta;
        this.agente = agente;
        this.nome = nome;
        this.saldo = saldo;
    }
}
